function genFullOpening(name, lifeNum, missingCount, dayMasterWX, gender, baziLinks) {
  var opening = genOpening(name, lifeNum, missingCount, dayMasterWX, gender);

  if (baziLinks && baziLinks.length > 0) {
    var doubleConfirmLinks = baziLinks.filter(function(l) { return l.isDoubleConfirm; });
    if (doubleConfirmLinks.length > 0) {
      opening += ' 特别值得关注的是，八字和生命数字在' + doubleConfirmLinks.map(function(l) { return l.wuXing; }).join('、') + '上达成了双重印证——这是' + (gender === 'female' ? '她' : '他') + '最需要后天培养的方向。';
    } else {
      opening += ' 八字给出的方向和生命数字有互补关系，两者结合来看，能更全面地了解' + (gender === 'female' ? '她' : '他') + '。';
    }
  }

  return opening;
}
