function genMissingWXDeep(name, gender, missingWX, lifeNum) {
  var pronoun = gender === 'female' ? '她' : '他';
  var d = missingWXDeep[missingWX];
  if (!d) return null;

  var numKey = d.lifeNumbers.indexOf(lifeNum) >= 0 ? lifeNum : 'default';

  var replace = function(s) {
    return s.replace(/\{name\}/g, name).replace(/\{pronoun\}/g, pronoun);
  };

  var fixKey = d.fix[numKey] ? numKey : Object.keys(d.fix)[0];
  var fixes = (d.fix[fixKey] || []).map(function(f) { return replace(f); });

  return {
    missingWX: missingWX,
    lifeNumbers: d.lifeNumbers,
    isDoubleConfirm: d.lifeNumbers.indexOf(lifeNum) >= 0,
    signal:    replace(d.signal[numKey]    || d.signal.default    || ''),
    crossLink: replace(d.crossLink[numKey] || d.crossLink.default || ''),
    fix:       fixes
  };
}

