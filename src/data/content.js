/**
 * 获取天生优势文本（含{name}/{pronoun}替换）
 * 如果前50字和性格文本相同，自动降级到 default
 */
function getNumberStrength(name, gender, lifeNum, dayMasterWX) {
  var pronoun = gender === 'female' ? '她' : '他';
  var numMap = numberContent[lifeNum] || numberContent[1];
  var entry = numMap[dayMasterWX] || numMap['default'];
  if (!entry) return '';
  var strength = entry.strength.replace(/\{name\}/g, name).replace(/\{pronoun\}/g, pronoun);

  // 和前50字比对，如果和性格相同则用 default
  var personality = getNumberPersonality(name, gender, lifeNum, dayMasterWX);
  if (personality && strength.substring(0, 50) === personality.substring(0, 50)) {
    var defEntry = numMap['default'];
    if (defEntry) {
      strength = defEntry.strength.replace(/\{name\}/g, name).replace(/\{pronoun\}/g, pronoun);
    }
  }
  return strength;
}
