/**
 * 生成三个方向的行动建议（事业/爱情/生活）
 */
function genLifeActions(name, gender, lifeNum, dayMasterWX) {
  var pronoun = gender === 'female' ? '她' : '他';
  var pool = lifeActions[lifeNum] || {};
  var data = pool[dayMasterWX] || pool['default'] || null;
  if (!data) return null;
  var replace = function(s) {
    return s.replace(/\{name\}/g, name).replace(/\{pronoun\}/g, pronoun);
  };
  return {
    career: { title: data.career.title, action: replace(data.career.action) },
    love:   { title: data.love.title,   action: replace(data.love.action)   },
    life:   { title: data.life.title,   action: replace(data.life.action)   }
  };
}

